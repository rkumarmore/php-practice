<p>As we know that each machine understand and deals in its different language or input so web-services are something which are required for inter communication between machines and to exchange data between them. In order to implement some set of restrictions over their communication some set of rules and regulations are defined which are known as web-services which basically defines the format and type of data that need to be exchanged and specifically a contract which both machines should be aware of before taking part in communication.</p>
<p>This communication system can be categorized into two types, namely Simple Object Access Protocol or SOAP, and Representational State Transfer or REST.</p>
<p>Following are the important differences between REST API and SOAP API.</p>
