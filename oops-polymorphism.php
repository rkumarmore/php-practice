- Polymorphism in OOPs is a concept that allows you to create classes with different functionalities in a single interface.
- Generally, it is of two types:
1. compile-time (overloading)
2. run time (overriding)
- Polymorphism (PHP does not support overloading) compile-time polymorphism.
- Polymorphism can be implemented in two ways,
1. Interfaces
2. Abstract classes